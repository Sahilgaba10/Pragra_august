package Practice_class;

public class break_and_continue {
    public static void main(String[] args) {
        for (int i=0;i<=10; i++){
            System.out.println(i);
            System.out.println("Java is Great");
            if (i==2){
                System.out.println("end of process");
                break;
            }
            System.out.println("ending....");

        }
    }
}
