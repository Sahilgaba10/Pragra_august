package practice_class;

public class Do_while_loop_sept19 {
    public static void main(String[] args) {

        //while loop
        int i = 1;
        while (i <= 3) {
            System.out.println(i);
            i++;
        }
        System.out.println("finish learning while loop");

        int it = 10;
        while (it <= 13) {
            System.out.println(it);
            it++;
        }
        System.out.println("end of second part");
        //do-while loop

        int b = 0;
        do {
            System.out.println(b);
            b++;
        }while (b<5);





    }
}