public class Sept16 {
    public static void main(String[] args) {
        System.out.println("welcome to my calculator");

        char input = '*';






        if(input == '+'){
            int x = 10;
            int y = 20;

            int sum = x+y;
            System.out.println("sum of x and y is" + sum);

        } else if (input == '-') {
            int x = 10;
            int y = 20;

            int diff = x-y;
            System.out.println("sum of x and y is" + diff);

        } else if (input == '*'){
            int x = 10;
            int y = 20;
            int multi = x*y;
            System.out.println("multi of x and y is" + multi);

        }


    }
}
